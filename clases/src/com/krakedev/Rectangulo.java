package com.krakedev;

public class Rectangulo {
	
	public int base;
	public int altura;
	
	public int CalcularArea() {
		int area;
		area= base * altura;
		return area ;
		
	}
	
	public int CalcularPerimetro() {
		int perimetro;
		int bs;
		int al;
		bs= base+base;
		al= altura+altura;
		perimetro= bs + al;
		return perimetro;
		
	}
	
	

}
